<?php
/**
 * Inlines the editor-only `window.kntntGpxBlocks` registry payload.
 *
 * Exposes the validated tile-provider and overlay registries to the block
 * editor so the GPX Map block's Inspector controls can enumerate them. Both
 * the per-block tile-provider dropdown (issue #79) and the overlay toggles
 * (issue #80) read from the same global; this enqueuer is the single source
 * the editor JS consults.
 *
 * The payload is JSON-encoded and attached as a `'before'` inline script on
 * the GPX Map block's editor handle, so it lands in the editor document
 * before the block's React entry runs and reads the global. API keys are
 * deliberately excluded â€” those are per-block (`attributes.tileApiKey`) and
 * never leave PHP except as the substituted `{KEY}` value in the rendered
 * tile URL.
 *
 * @package Kntnt\Gpx_Blocks
 * @since   1.0.0
 */

declare( strict_types = 1 );

namespace Kntnt\Gpx_Blocks\Bootstrap;

use Kntnt\Gpx_Blocks\Plugin;
use Kntnt\Gpx_Blocks\Rendering\Tile_Layer_Registry;

/**
 * Enqueues the editor-only `window.kntntGpxBlocks` registry payload.
 *
 * Constructed once by Plugin and bound to `enqueue_block_editor_assets` so
 * the global is set up on every editor request â€” post, site, widgets, and
 * navigation â€” before the GPX Map block's editor script reads from it.
 *
 * @package Kntnt\Gpx_Blocks
 * @since 1.0.0
 */
final class Editor_Data_Enqueuer {

	/**
	 * Editor script handle generated by `register_block_type()` for the GPX
	 * Map block.
	 *
	 * Follows WordPress's `generate_block_asset_handle()` convention:
	 * `<namespace>-<slug>-editor-script`. The block.json `name` is
	 * `kntnt-gpx-blocks/map`, so the handle is
	 * `kntnt-gpx-blocks-map-editor-script`.
	 *
	 * @since 1.0.0
	 * @var string
	 */
	private const MAP_EDITOR_HANDLE = 'kntnt-gpx-blocks-map-editor-script';

	/**
	 * Constructs the enqueuer with an injectable registry.
	 *
	 * Tests inject a synthetic registry; production wiring uses a fresh
	 * `Tile_Layer_Registry` per request (its filter-evaluation cache is
	 * per-instance and that is fine for a single editor render).
	 *
	 * @since 1.0.0
	 *
	 * @param Tile_Layer_Registry|null $registry Tile-layer registry, or null
	 *                                           to construct a default one
	 *                                           lazily inside `enqueue()`.
	 */
	public function __construct( private readonly ?Tile_Layer_Registry $registry = null ) {}

	/**
	 * Builds the editor data payload and inlines it as a `before` script.
	 *
	 * Wired to `enqueue_block_editor_assets`. Reads the validated provider
	 * and overlay registries, strips internal-only fields that the editor
	 * does not need (notably `requiresKey`, `signupUrl`, and `subdomains`,
	 * which are render-time concerns), JSON-encodes the result, and emits
	 * it as `window.kntntGpxBlocks = {...}`. When JSON encoding fails or
	 * the inline-script call is unavailable, a warning is logged and the
	 * method returns silently â€” the editor falls back to an empty registry
	 * and the overlay panel collapses to nothing.
	 *
	 * @since 1.0.0
	 */
	public function enqueue(): void {

		// Resolve the registry from the constructor or build a default.
		$registry = $this->registry ?? new Tile_Layer_Registry();

		// Compose the payload from the validated registries. Both lists are
		// keyed by id; the editor reads `id` and `label` per entry.
		$payload = [
			'providers' => self::shape_providers( $registry->get_providers() ),
			'overlays'  => self::shape_overlays( $registry->get_overlays() ),
		];

		// Encode the payload. wp_json_encode() returns false on failure, in
		// which case we log a warning and bail rather than emit an invalid
		// global.
		$json = wp_json_encode( $payload );
		if ( ! is_string( $json ) ) {
			Plugin::warning( 'Editor_Data_Enqueuer: wp_json_encode failed; window.kntntGpxBlocks not emitted.' );
			return;
		}

		// Attach the inline script to the GPX Map block's editor handle so
		// the global is set up before the React entry reads it. Position
		// `before` ensures the assignment runs before the entry's IIFE.
		$inline = sprintf( 'window.kntntGpxBlocks = %s;', $json );
		wp_add_inline_script( self::MAP_EDITOR_HANDLE, $inline, 'before' );

	}

	/**
	 * Shapes the provider registry for the editor payload.
	 *
	 * Strips internal-only fields the editor's UI does not need. The shape
	 * is deliberately minimal so a future change to the registry's internal
	 * record type does not silently leak through the editor data.
	 *
	 * @since 1.0.0
	 *
	 * @param array<string, array{
	 *     label: string,
	 *     url: string,
	 *     attribution: string,
	 *     maxZoom: int,
	 *     requiresKey: bool,
	 *     signupUrl?: string,
	 *     subdomains?: list<string>,
	 * }> $providers Validated provider records keyed by id.
	 *
	 * @return array<string, array{ label: string, requiresKey: bool, signupUrl?: string }>
	 */
	private static function shape_providers( array $providers ): array {

		$out = [];

		foreach ( $providers as $id => $record ) {
			$entry = [
				'label'       => $record['label'],
				'requiresKey' => $record['requiresKey'],
			];
			if ( isset( $record['signupUrl'] ) ) {
				$entry['signupUrl'] = $record['signupUrl'];
			}
			$out[ $id ] = $entry;
		}

		return $out;

	}

	/**
	 * Shapes the overlay registry for the editor payload.
	 *
	 * Carries everything the editor preview's `L.tileLayer()` call needs to
	 * mount the overlay on top of the hardcoded base map in
	 * `MapEditorPreview` â€” URL, attribution, maxZoom, and the optional
	 * subdomains list. `label` drives the inspector toggle. Overlays in v1
	 * carry no API key, so no `{KEY}` substitution is needed; if a future
	 * overlay does, the editor will need to follow the same per-block-key
	 * substitution path that the frontend provider already uses.
	 *
	 * @since 1.0.0
	 *
	 * @param array<string, array{
	 *     label: string,
	 *     url: string,
	 *     attribution: string,
	 *     maxZoom: int,
	 *     subdomains?: list<string>,
	 * }> $overlays Validated overlay records keyed by id.
	 *
	 * @return array<string, array{
	 *     label: string,
	 *     url: string,
	 *     attribution: string,
	 *     maxZoom: int,
	 *     subdomains?: list<string>,
	 * }>
	 */
	private static function shape_overlays( array $overlays ): array {

		$out = [];

		foreach ( $overlays as $id => $record ) {
			$entry = [
				'label'       => $record['label'],
				'url'         => $record['url'],
				'attribution' => $record['attribution'],
				'maxZoom'     => $record['maxZoom'],
			];
			if ( isset( $record['subdomains'] ) ) {
				$entry['subdomains'] = $record['subdomains'];
			}
			$out[ $id ] = $entry;
		}

		return $out;

	}

}
